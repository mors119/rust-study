fn exam_1() {
    const INPUT: &str = "
        a3167176531330624919225119674426574742355349194934
        b6983520312774506326239578318016984801869478851843
        c5861560789112949495459501737958331952853208805511
    ";
    let v = get_num_vector(INPUT);
    assert_eq!(vec![3, 1, 6, 7, 1], &v[0..5]);
}

fn get_num_vector(s: &str) -> Vec<u64> {
    s.chars() // string을 char 배열로 변환
        .filter_map(|c| {
            // Some 반환
            if c.is_numeric() {
                // 숫자만 거르기
                c.to_digit(10).map(|n| n as u64) // 10진수로 변환 및 u64타입으로 변환
            } else {
                None
            }
        })
        .collect()
}

fn exam_2() {
    const INPUT: &str = r"37107287533902102798797998220837590246510135740250
    46376937677490009712648124896970078050417018260538
    74324986199524741059474233309513058123726617309629
    91942213363574161572522430563301811072406154908250
    23067588207539346171171980310421047513778063246676";

    let sum = cal_sum(INPUT);
    assert_eq!(271, sum);
}

fn cal_sum(input: &str) -> u64 {
    input
        .lines()
        .map(|line| line.trim()) // 공백 지우기
        .map(|line| &line[0..2]) // 0, 1 번만 부르기
        .filter_map(|s| s.parse::<u64>().ok()) // parse 터보피쉬 문법(::< >)으로 타입 변환 .ok()는 Result -> Option으로 바꿔주는 문법
        .sum() // 더하기
}

fn exam_3() {
    // 숫자로 모두 변환 후 짝수만 더하기
    let input: &str = "73167176531330624919225119674426574742355349194934";

    let sum: u32 = input
        .chars() // 문자열을 하나씩 꺼낸다.
        .filter_map(|c| c.to_digit(10)) // to_digit은 문자면 Some 아니면 None
        .filter(|n| n % 2 == 0)
        .sum();

    assert_eq!(72, sum);
}

