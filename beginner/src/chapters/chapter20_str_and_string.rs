use std::vec;

/* map / filter_map
  map: 변환만 함 (반환 타입은 내부 클로저가 정함)
  filter_map: 변환 + None 제거 (Option을 반환하고 그 중 Some만 반환)
*/

/* =========================================================
 * 1. Escape Sequence (이스케이프 문자)
 * =========================================================
 *
 * \n   : 줄바꿈 (Line Feed, Unix/Linux/macOS)
 * \r   : 캐리지 리턴 (Carriage Return, 구형 Mac)
 * \r\n : 줄바꿈 (Windows)
 * \t   : 탭 (Tab)
 * \\   : 백슬래시 (\)
 * \0   : 널 문자 (Null terminator)
 * \'   : 작은 따옴표
 *
 * ※ 일반 문자열에서는 위 이스케이프 문자가 해석된다.
 */

/* =========================================================
 * 2. Unicode 문자열 (&str)
 * =========================================================
 *
 * ✔ 일반 문자열
 *   "hello\n"
 *   - 타입: &str (UTF-8)
 *   - 이스케이프 문자 해석됨
 *   - \n → 실제 줄바꿈
 *
 * ✔ 원시 문자열 (raw string)
 *   r#"hello\n"#
 *   - 타입: &str (UTF-8)
 *   - 이스케이프 문자 해석 안 함
 *   - \n → '\' + 'n' 그대로 유지
 *
 * ✔ 특징
 *   - 유니코드 지원 (한글, 이모지 등 가능)
 *   - Rust에서 가장 기본적으로 사용하는 문자열 타입
 */

/* =========================================================
 * 3. 바이트 문자열 (&[u8])
 * =========================================================
 *
 * ✔ 바이트 문자열
 *   b"hello\n"
 *   - 타입: &[u8]
 *   - ASCII 기반 바이트 배열
 *   - 이스케이프 문자 해석됨
 *   - \n → 10 (newline)
 *
 * ✔ 원시 바이트 문자열
 *   br#"hello\n"#
 *   - 타입: &[u8]
 *   - 이스케이프 문자 해석 안 함
 *   - '\'와 'n'이 그대로 바이트로 저장됨
 *
 * ✔ 특징
 *   - 유니코드 ❌ (ASCII만 가능)
 *   - 문자열이 아니라 "바이트 배열"
 *   - 네트워크, 파일, 바이너리 처리에 사용
 */

/* =========================================================
 * 4. C 스타일 문자열 (&CStr) - FFI 용도
 * =========================================================
 *
 * ✔ C 문자열
 *   c"hello\n"
 *   - 타입: &CStr
 *   - 내부적으로 null 종료 (\0 포함)
 *   - 이스케이프 문자 해석됨
 *
 * ✔ 원시 C 문자열
 *   cr#"hello\n"#
 *   - 타입: &CStr
 *   - 이스케이프 문자 해석 안 함
 *
 * ✔ 특징
 *   - C 라이브러리 연동 (FFI)에서 사용
 *   - 일반 Rust 코드에서는 거의 사용하지 않음
 */

/* =========================================================
 * 5. 핵심 정리
 * =========================================================
 *
 * "..."   → Unicode 문자열 (&str) → 일반 문자열 (99% 사용)
 * r"..."  → Unicode raw 문자열 (&str) → escape 귀찮을 때
 *
 * b"..."  → 바이트 배열 (&[u8]) → 바이너리 처리할 때
 * br"..." → raw 바이트 배열
 *
 * c"..."  → C 문자열 (&CStr) → C랑 붙을 때만
 * cr"..." → raw C 문자열
 *
 * ※ r이 붙으면 → 이스케이프 문자 무시
 */

